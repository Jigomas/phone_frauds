def _get_model():
    global _model
    if _model is None:
        from sentence_transformers import SentenceTransformer
        _model = SentenceTransformer("paraphrase-multilingual-mpnet-base-v2")
    return _model


def _get_embeddings():
    global _fraud_embeddings, _legit_embeddings
    if _fraud_embeddings is None:
        model = _get_model()
        _fraud_embeddings = model.encode(FRAUD_TEMPLATES, normalize_embeddings=True)
        _legit_embeddings = model.encode(LEGIT_TEMPLATES, normalize_embeddings=True)
    return _fraud_embeddings, _legit_embeddings


def _cosine_sim(a: np.ndarray, b: np.ndarray) -> np.ndarray:
    # Оба вектора уже нормированы (L2), поэтому косинусное сходство = просто скалярное произведение
    if a.ndim == 1:
        a = a[np.newaxis, :]
    return (a @ b.T).squeeze()


def semantic_score(text: str, chunk_size: int = 200) -> float:
    """
    Возвращает оценку от 0 до 1.
    Разбивает текст на перекрывающиеся чанки, сравнивает каждый с шаблонами
    мошеннических и легитимных звонков, возвращает нормализованный перевес мошенничества.
    """
    if not text or len(text.strip()) < 20:
        return 0.0

    model = _get_model()
    fraud_emb, legit_emb = _get_embeddings()

    # Разбиваем транскрипцию на перекрывающиеся чанки, чтобы не терять контекст на стыках
    words = text.split()
    step = chunk_size // 2
    chunks = []
    for i in range(0, max(1, len(words) - chunk_size + 1), step):
        chunk = " ".join(words[i : i + chunk_size])
        if chunk:
            chunks.append(chunk)
    if not chunks:
        chunks = [text]

    chunk_embs = model.encode(chunks, normalize_embeddings=True)  # (C, D)

    # For each chunk: similarity to fraud / legit templates
    fraud_sims = chunk_embs @ fraud_emb.T   # (C, F)
    legit_sims = chunk_embs @ legit_emb.T   # (C, L)

    # Берём максимум по шаблонам (лучшее совпадение), затем усредняем по чанкам
    fraud_score = float(np.mean(np.max(fraud_sims, axis=1)))
    legit_score = float(np.mean(np.max(legit_sims, axis=1)))

    # Перевес: насколько текст ближе к мошенническим шаблонам, чем к легитимным.
    # Нейтральный текст (advantage≈0) → score≈0.5; мошеннический → >0.5; легитимный → <0.5.
    advantage = fraud_score - legit_score
    return float(np.clip((advantage + 0.5) / 1.0, 0.0, 1.0))
